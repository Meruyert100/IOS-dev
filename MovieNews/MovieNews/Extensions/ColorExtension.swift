import Foundation

import UIKit

extension UIColor {

    class var tintColor: UIColor {
        return UIColor.orange;
    }

    class var backgroundColor: UIColor {
        return UIColor(red: 40.0 / 255.0, green: 47.0 / 255.0, blue: 55.0 / 255.0, alpha: 1.0);
    }

}
